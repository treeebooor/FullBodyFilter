def rcnn_detect(image):
    '''
    run Mask-RCNN detection on image

    Args:
        image to detect

    Returns:
        Person mask if there is a single person, [] otherwise
    '''

    results = model.detect([image], False, verbose=0)  #results is a list of dictionaries for each frame passed into detect(), in this case only 1 image is passed
    r = results[0]  #r is the dictionary of info for the frame with keys: rois, masks, class_ids, scores
    N = r["rois"].shape[0]

    #count how many people are in frame
    people_count  = 0
    for i in range(N):
        if r['class_ids'][i] == 1:
            people_count += 1
            mask = r['masks'][:,:, i]*255

    #return person mask and image if only one person 
    if people_count == 1:
        return mask

    return []

